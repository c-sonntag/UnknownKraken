#include <ei/ei.h>

/**
 * output:
 * Oops ! (thread 140164974479168)
 *    at main (stacktrace_example2.c:33)
 */
int main() {
    ei_init_or_die();

    ei_stacktrace_push_msg("Oops !");

    if (ei_stacktrace_is_filled()) {
        ei_stacktrace_print();
    }

    ei_uninit();

    return EXIT_SUCCESS;
}
